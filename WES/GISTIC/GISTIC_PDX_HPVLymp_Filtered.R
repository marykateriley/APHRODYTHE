########################################################## READ DATA ##########################################################

all.lesions <- "./0_data/GISTIC/PDX_HPVLymp_Rem/all_lesions.conf_99_renamed.txt"
amp.genes <- "./0_data/GISTIC/PDX_HPVLymp_Rem/amp_genes.conf_99.txt"
del.genes <- "./0_data/GISTIC/PDX_HPVLymp_Rem/del_genes.conf_99.txt"
scores.gis <- "./0_data/GISTIC/PDX_HPVLymp_Rem/scores.gistic"

########################################################## SET VARIABLES ##########################################################

title <- "HN0039A_Filtered_PDX"
plot_title <- "GISTIC PDX: Filtered for HPV and Lymphoma"

########################################################## GISTIC MAFTOOLS ##########################################################
# Summarise output files generated by GISTIC
laml.gistic = readGistic(gisticAllLesionsFile = all.lesions, gisticAmpGenesFile = amp.genes, gisticDelGenesFile = del.genes, gisticScoresFile = scores.gis)
laml.gistic
write.GisticSummary(laml.gistic,paste0(pipelines,"GISTICSummary"))

# Visualise GISTIC results
# Genome plot 
pdf(paste0(output,title,"_GISTIC_genomeplot.pdf"),width = 10, height = 8)
gisticChromPlot(gistic = laml.gistic, markBands = "all", 
                txtSize = 1.2, cytobandTxtSize = 0.6,
                fdrCutOff = 0.05, ref.build = "hg38")
dev.off()

# Bubble plot
png(paste0(output,title,"_GISTIC_bubbleplot.png"),width = 7.5, height = 6, units = "in", res = 200)
gisticBubblePlot(gistic = laml.gistic, markBands = "all")
dev.off()

